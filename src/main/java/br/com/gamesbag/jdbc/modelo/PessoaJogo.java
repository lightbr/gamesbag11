package br.com.gamesbag.jdbc.modelo;

public class PessoaJogo {
	
	int idPessoa;
	int idJogo;
	float notaPessoa;
	int quero;
	int joguei;
	int jogando;
	int terminei;
	public int getIdPessoa() {
		return idPessoa;
	}
	public void setIdPessoa(int idPessoa) {
		this.idPessoa = idPessoa;
	}
	public int getIdJogo() {
		return idJogo;
	}
	public void setIdJogo(int idJogo) {
		this.idJogo = idJogo;
	}
	public float getNotaPessoa() {
		return notaPessoa;
	}
	public void setNotaPessoa(float notaPessoa) {
		this.notaPessoa = notaPessoa;
	}
	public int getQuero() {
		return quero;
	}
	public void setQuero(int quero) {
		this.quero = quero;
	}
	public int getJoguei() {
		return joguei;
	}
	public void setJoguei(int joguei) {
		this.joguei = joguei;
	}
	public int getJogando() {
		return jogando;
	}
	public void setJogando(int jogando) {
		this.jogando = jogando;
	}
	public int getTerminei() {
		return terminei;
	}
	public void setTerminei(int terminei) {
		this.terminei = terminei;
	}
	
	
	

}
